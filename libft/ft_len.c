#include "libft.h"

/*
** counts n numbers
*/

int		ft_len(int n)
{
	int len;

	len = 0;
	if (n < 0)
		len++;
	while (n / 10 != 0)
	{
		n = n / 10;
		len++;
	}
	len++;
	return (len);
}
